#Arithmetic operators - simple calculations

x = 34
y = 20

print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%y)#Modulus - return reminder

#comparison operator -compare values
print(x<y)
print(x>y)
print(x<=y)
print(x>=y)
print(x==y) #== - equal to
print(x!=y) #!--not equal to

#assignment operators
number = 10
print(number)


#operator precedence - "BODMAS"

number += 2
print(number)

#logical Operators - and, or, not
num1 = 60
num2 = 12
print(num1 == num2 and num1 > num2)
print(num1 == num2 or num1 > num2)
print(not(num1 == num2 or num1 > num2))

